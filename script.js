document.addEventListener('keydown', function(e) {
    // Ctrl + G = Generate CV
    if (e.ctrlKey && e.key === 'g') {
        e.preventDefault();
        generateCV();
    }
    // Ctrl + S = Save Draft
    if (e.ctrlKey && e.key === 's') {
        e.preventDefault();
        saveToLocalStorage();
    }
    // Ctrl + P = Print
    if (e.ctrlKey && e.key === 'p') {
        e.preventDefault();
        printCV();
    }
    // Escape = Close Modal
    if (e.key === 'Escape') {
        closeLogin();
    }
});

